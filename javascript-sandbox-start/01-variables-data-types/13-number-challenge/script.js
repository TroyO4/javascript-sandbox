const x = Math.floor(Math.random() * 101);
const y = Math.floor(Math.random() * 51);

const sum = x + y;
const diff = x - y;
const prod = x * y;
const q = x / y;

console.log(`${x} + ${y} = ${sum}`);
console.log(`${x} - ${y} = ${diff}`);
console.log(`${x} * ${y} = ${prod}`);
console.log(`${x} / ${y} = ${q}`);
